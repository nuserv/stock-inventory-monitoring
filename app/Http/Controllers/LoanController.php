    public function table(Request $request)
    {
        $loans = Loan::select('branches.branch', 'loans.updated_at', 'items.item', 'loans.status')
                ->where('to_branch_id', Auth::user()->branch->id)
                ->where('status', 'approved')
                ->join('branches', 'loans.from_branches_id', '=', 'branches.id')
                ->join('items', 'loans.items_id', '=', 'items.id')
                ->get();

        return Datatables::of($loans)

        ->make(true);
    }

